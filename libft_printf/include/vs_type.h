#ifndef VS_TYPE_H
# define VS_TYPE_H

typedef signed char			t_int8;
typedef signed short		t_int16;
typedef signed int			t_int32;
typedef signed long int		t_int64;

typedef unsigned char		t_ui8;
typedef unsigned short		t_ui16;
typedef unsigned int		t_ui32;
typedef unsigned long int	t_ui64;

typedef float				t_float32;
typedef double				t_float64;
typedef long double			t_float128;

#endif