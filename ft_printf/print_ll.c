#include "ft_printf.h"

ssize_t print_ll(long long n)
{
	if (n == LLONG_MAX)
		return (write(1, "-9223372036854775808", 20));
	if (n < 0)
		return(write(1, "-", 1) + print_ull(-n));
	return (print_ull(n));
}
